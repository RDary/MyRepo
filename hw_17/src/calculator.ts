export class Calculator {
  constructor() {}

  public addition(firstNumber: number, secondNumber: number) {
    return firstNumber + secondNumber;
  }

  public subtraction(firstNumber: number, secondNumber: number) {
    return firstNumber - secondNumber;
  }

  public multiplication(firstNumber: number, secondNumber: number) {
    return firstNumber * secondNumber;
  }

  public division(firstNumber: number, secondNumber: number) {
    return firstNumber / secondNumber;
  }
}
